/// Defines the unique identifier code for each Turkic world flag.
///
/// This enum includes independent states, autonomous republics,
/// historical empires, and international Turkic organizations.
enum TurkicFlagCode {
  // --- Independent Turkic States ---
  /// Republic of Turkey (Türkiye Cumhuriyeti).
  tr,

  /// Republic of Azerbaijan (Azerbaycan Cumhuriyeti).
  az,

  /// Republic of Kazakhstan (Kazakistan Cumhuriyeti).
  kz,

  /// Kyrgyz Republic (Kırgızistan Cumhuriyeti).
  kg,

  /// Republic of Uzbekistan (Özbekistan Cumhuriyeti).
  uz,

  /// Republic of Turkmenistan (Türkmenistan Cumhuriyeti).
  tm,

  /// Turkish Republic of Northern Cyprus (Kuzey Kıbrıs Türk Cumhuriyeti).
  kktc,

  // --- Autonomous Turkic Republics and Regions ---
  /// Altai Republic (Altay Cumhuriyeti).
  altai,

  /// Republic of Bashkortostan (Başkurdistan Cumhuriyeti).
  bashkortostan,

  /// Chuvash Republic (Çuvaşistan Cumhuriyeti).
  chuvashia,

  /// Autonomous Territorial Unit of Gagauzia (Gagauzya Özerk Yeri).
  gagauzia,

  /// Republic of Khakassia (Hakasya Cumhuriyeti).
  khakassia,

  /// Karachay-Cherkess Republic (Karaçay-Çerkesya Cumhuriyeti).
  karachayCherkessia,

  /// Republic of Karakalpakstan (Karakalpakistan Cumhuriyeti).
  karakalpakstan,

  /// Nakhchivan Autonomous Republic (Nahçıvan Özerk Cumhuriyeti).
  nakhchivan,

  /// Sakha Republic / Yakutia (Saka / Yakutistan Cumhuriyeti).
  sakha,

  /// Republic of Tatarstan (Tataristan Cumhuriyeti).
  tatarstan,

  /// Tuva Republic (Tuva Cumhuriyeti).
  tuva,

  /// East Turkestan / Uyghur flag (Doğu Türkistan / Uygur Bayrağı).
  eastTurkestan,

  /// Crimean Tatars flag (Kırım Tatar Türkleri Bayrağı - Gökbayrak & Tarak Tamga).
  crimeanTatars,

  /// Dagestan / Kumyk Turks flag (Dağıstan / Kumuk Türkleri Bayrağı).
  dagestan,

  // --- Organizations and Historical Empires ---
  /// Organization of Turkic States (Türk Devletleri Teşkilatı - OTS).
  ots,

  /// Turan Flag / Union (Turan Birliği Bayrağı).
  turan,

  /// Great Hun Empire / Xiongnu (Büyük Hun İmparatorluğu - Ejderha / Altın Kartal).
  hun,

  /// Western Hun Empire (Batı Hun İmparatorluğu).
  westernHun,

  /// European Hun Empire (Avrupa Hun İmparatorluğu).
  europeanHun,

  /// Hephthalite Empire / White Huns (Ak Hun İmparatorluğu).
  whiteHun,

  /// Göktürk Khaganate (Göktürk Kağanlığı - Kök Türk Gök Bayrak ve Kurt Başı).
  gokturk,

  /// Avar Khaganate (Avar İmparatorluğu).
  avar,

  /// Khazar Khaganate (Hazar İmparatorluğu).
  khazar,

  /// Uyghur Khaganate (Uygur Devleti).
  uyghurKhaganate,

  /// Kara-Khanid Khanate (Karahanlılar).
  karakhanid,

  /// Ghaznavid Empire (Gazneliler).
  ghaznavid,

  /// Great Seljuk Empire (Büyük Selçuklu İmparatorluğu - Çift Başlı Kartal).
  seljuk,

  /// Khwarazmian Empire (Harzemşahlar).
  khwarazmian,

  /// Golden Horde (Altınordu Devleti).
  goldenHorde,

  /// Timurid Empire (Büyük Timur İmparatorluğu).
  timurid,

  /// Mughal Empire (Babür İmparatorluğu).
  mughal,

  /// Ottoman Empire (Osmanlı İmparatorluğu).
  ottoman,

  /// Iraqi Turkmen (Irak Türkmenleri).
  iraqiTurkmen,

  /// Syrian Turkmen (Suriye Türkmenleri).
  syrianTurkmen,

  /// Western Thrace Turks (Batı Trakya Türkleri - Batı Trakya Bağımsız Türk Cumhuriyeti).
  westernThrace,
}
